class Phone
{	
	private String name,type,os;
	private short ram;
	private float price;
	
	public void setName(String a)
	{
		name=a;
	}
	public String getName()
	{
		return name;
	}
	
	public void setType(String b)
	{
		type=b;
	}
	public String getType()
	{
		return type;
	}
	
	public void setOs(String c)
	{
		os=c;
	}
	public String getOs()
	{
		return os;
	}
	
	public void setRam(short d)
	{
		ram=d;
	}
	public short getRam()
	{
		return ram;
	}
	
	public void setPrice(float e)
	{
		price=e;
	}
	public float getPrice()
	{
		return price;
	}

}
